package java_Basic;

public class Variables {

    public static void main(String[] args) {
        /*
         * Byte
         * Min: 128
         * Max: 127
         * Default: 0
         */
        byte myByte = 100;
        System.out.println("Byte Value: " + myByte);

        /*
         * Short
         * Min: -32,768
         * Max: 32,767
         * Default: 0
         */
        short myShort = 10000;
        System.out.println("Short Value: " + myShort);

        /*
         * int
         * Min: -2,147,283,648
         * Max: 2,147,283,648
         * Default: 0
         */



    }

}
